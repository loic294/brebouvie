     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $i = $request->all();

        $role = new Role;
        $role->nom = $i['nom'];
        $role->slug = str_slug($i['nom']);
        $role->userType = $i['userType'];
        $role->save();

        return redirect()->back()->with('success_message', 'Le rôle a été ajouté.');

    }

